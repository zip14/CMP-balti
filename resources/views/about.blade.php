                            În anul 1981, în baza Ordinului nr. 199-6 din 16.06.1981 al Ministerului Învăţămîntului Superior şi Mediu de Specialitate al RSSM, Şcoala Muzicală medie de specialitate a fost reorganizată în Şcoala Pedagogică medie de specialitate.<br>
