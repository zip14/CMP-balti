                        <p>La 1 septembrie 1969, în baza Ordinului nr. 350 din 20.06.1969 al Ministerului Culturii, în Bălţi se formează Şcoala medie specială de muzică, colectivul pedagogic fiind format din director, cinci profesori titulari, cinsprezece profesori cumularzi şi o sută douăzeci de elevi înmatriculaţi la secţiile cu frecvenţă la zi şi frecvenţă redusă.</p>
                        <p>În primii ani de înfiinţare ca instituţie de învăţămînt mediu de specialitate, procesul instructiv-educativ a demarat doar în cadrul a  cinci catedre şi specializări respective: Pian, Instrumente cu corzi, Instrumente populare, Instrumente aerofone, Dirijare corală.</p>
